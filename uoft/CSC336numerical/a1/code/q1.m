format compact
%f(x) = (a+x)^(1/4) - a^(1/4)
%f'(x) = 1/4*(a+x)^(-3/4)

a = 1;
x = 10.^[-20:1:20];
f0 = (a+x).^(1/4) - a^(1/4);
fp = 1/4*(a+x).^(-3/4);
k0 = x.*fp./f0;

f1 = x./(((a+x).^(1/4) + a^(1/4)).*((a+x).^(1/2) + a^(1/2)));
k1 = x.*fp./f1;

disp('x           f0             f1          k0             k1             rel.err.')
for i = 1:41
    fprintf('%9.2e %12.5e %12.5e %12.5e %12.5e %10.2e\n', ...
        x(i), f0(i), f1(i), k0(i), k1(i), (f0(i)-f1(i))/f1(i));
end
